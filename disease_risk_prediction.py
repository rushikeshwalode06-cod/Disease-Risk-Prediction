import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('medical_disease_prediction_dataset.csv')

data

data['gender'] = data['gender'].map({'Male':0,'Female':1})
data['physical_activity'] = data['physical_activity'].map({'Low':0,'High':1,'Moderate':2})

data

X = data.drop('disease_risk',axis=1)

X

y = data.disease_risk

y

from sklearn.model_selection import train_test_split

X_train,X_test,y_train,y_test = train_test_split(X,y,test_size=0.2,random_state=0)

X_train.shape

X_test.shape

from sklearn.ensemble import RandomForestClassifier

model = RandomForestClassifier(n_estimators=5)

model.fit(X_train,y_train)

y_test

y_pred = model.predict(X_test)

y_pred

from sklearn.metrics import confusion_matrix,classification_report,ConfusionMatrixDisplay

print(confusion_matrix(y_test,y_pred))

cm = confusion_matrix(y_test,y_pred)

cm

disp = ConfusionMatrixDisplay(confusion_matrix=cm,display_labels=model.classes_)
disp.plot()
plt.grid(False)
plt.show()

print(classification_report(y_test,y_pred))

def disease_risk_prediction (age,gender,bmi,blood_pressure,cholesterol,glucose_level,smoking,alcohol_intake,physical_activity):
    prediction= model.predict([[age,gender,bmi,blood_pressure,cholesterol,glucose_level,smoking,alcohol_intake,physical_activity]])

    if prediction == 1:
        return "Disease Risk Present ⚠️"
    else :
        return "You are Healthy ✅"

